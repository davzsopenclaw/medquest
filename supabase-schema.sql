-- Auto-update updated_at
CREATE OR REPLACE FUNCTION update_updated_at()
RETURNS TRIGGER AS $$
BEGIN NEW.updated_at = NOW(); RETURN NEW; END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER profiles_updated_at BEFORE UPDATE ON profiles FOR EACH ROW EXECUTE FUNCTION update_updated_at();
CREATE TRIGGER questions_updated_at BEFORE UPDATE ON questions FOR EACH ROW EXECUTE FUNCTION update_updated_at();
CREATE TRIGGER topic_mastery_updated_at BEFORE UPDATE ON topic_mastery FOR EACH ROW EXECUTE FUNCTION update_updated_at();

-- Compute mastery score after each attempt
CREATE OR REPLACE FUNCTION update_topic_mastery()
RETURNS TRIGGER AS $$
DECLARE
  q_system question_system;
  q_topic TEXT;
BEGIN
  SELECT system, topic INTO q_system, q_topic FROM questions WHERE id = NEW.question_id;
  
  INSERT INTO topic_mastery (user_id, system, topic, attempts, correct, mastery_score, mastery_level, last_attempted)
  VALUES (
    NEW.user_id, q_system, q_topic, 1,
    CASE WHEN NEW.is_correct THEN 1 ELSE 0 END,
    CASE WHEN NEW.is_correct THEN 0.5 ELSE 0.1 END,
    CASE WHEN NEW.is_correct THEN 'learning' ELSE 'unstarted' END,
    NOW()
  )
  ON CONFLICT (user_id, system, topic) DO UPDATE SET
    attempts = topic_mastery.attempts + 1,
    correct = topic_mastery.correct + CASE WHEN NEW.is_correct THEN 1 ELSE 0 END,
    mastery_score = LEAST(1.0, (topic_mastery.correct + CASE WHEN NEW.is_correct THEN 1 ELSE 0 END)::FLOAT / (topic_mastery.attempts + 1)),
    mastery_level = CASE
      WHEN (topic_mastery.correct + CASE WHEN NEW.is_correct THEN 1 ELSE 0 END)::FLOAT / (topic_mastery.attempts + 1) >= 0.85 THEN 'mastered'
      WHEN (topic_mastery.correct + CASE WHEN NEW.is_correct THEN 1 ELSE 0 END)::FLOAT / (topic_mastery.attempts + 1) >= 0.6 THEN 'familiar'
      WHEN topic_mastery.attempts + 1 > 0 THEN 'learning'
      ELSE 'unstarted'
    END,
    last_attempted = NOW(),
    updated_at = NOW();
    
  RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER after_answer_attempt 
AFTER INSERT ON answer_attempts 
FOR EACH ROW EXECUTE FUNCTION update_topic_mastery();

-- Award XP after correct answer
CREATE OR REPLACE FUNCTION award_xp_for_attempt()
RETURNS TRIGGER AS $$
DECLARE
  xp_amount INTEGER;
  q_difficulty difficulty_tier;
BEGIN
  IF NOT NEW.is_correct THEN RETURN NEW; END IF;
  
  SELECT difficulty INTO q_difficulty FROM questions WHERE id = NEW.question_id;
  
  xp_amount := CASE q_difficulty
    WHEN 'Tier1_Recall' THEN 10
    WHEN 'Tier2_Application' THEN 20
    WHEN 'Tier3_Integration' THEN 35
    ELSE 10
  END;
  
  -- Speed bonus
  IF NEW.time_taken_seconds IS NOT NULL AND NEW.time_taken_seconds < 30 THEN
    xp_amount := xp_amount + 5;
  END IF;
  
  UPDATE NEW SET xp_earned = xp_amount;
  
  UPDATE profiles SET 
    xp = xp + xp_amount,
    questions_answered = questions_answered + 1,
    questions_correct = questions_correct + 1,
    level = GREATEST(1, FLOOR(SQRT(xp / 100)) + 1)
  WHERE id = NEW.user_id;
  
  INSERT INTO xp_transactions (user_id, amount, reason, reference_id)
  VALUES (NEW.user_id, xp_amount, 'correct_answer', NEW.id);
  
  RETURN NEW;
END;
$$ LANGUAGE plpgsql;

-- Update wrong answer stats too
CREATE OR REPLACE FUNCTION update_profile_stats()
RETURNS TRIGGER AS $$
BEGIN
  UPDATE profiles SET 
    questions_answered = questions_answered + 1
  WHERE id = NEW.user_id AND NOT NEW.is_correct;
  RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER after_answer_xp AFTER INSERT ON answer_attempts FOR EACH ROW EXECUTE FUNCTION award_xp_for_attempt();
CREATE TRIGGER after_answer_stats AFTER INSERT ON answer_attempts FOR EACH ROW WHEN (NOT NEW.is_correct) EXECUTE FUNCTION update_profile_stats();
