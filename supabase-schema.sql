-- =============================================
-- LEADERBOARD VIEW
-- =============================================

CREATE VIEW leaderboard AS
SELECT 
  p.id,
  p.display_name,
  p.xp,
  p.level,
  p.streak_current,
  p.questions_answered,
  CASE 
    WHEN p.questions_answered > 0 
    THEN ROUND((p.questions_correct::NUMERIC / p.questions_answered) * 100, 1)
    ELSE 0 
  END AS accuracy_percent,
  RANK() OVER (ORDER BY p.xp DESC) AS rank
FROM profiles p
WHERE p.is_whitelisted = TRUE OR p.email LIKE '%@u.nus.edu'
ORDER BY p.xp DESC;

