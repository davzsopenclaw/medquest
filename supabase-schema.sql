CREATE POLICY "Users can update own profile" ON profiles FOR UPDATE USING (auth.uid() = id);
CREATE POLICY "Users can insert own profile" ON profiles FOR INSERT WITH CHECK (auth.uid() = id);
CREATE POLICY "Users can upsert own profile" ON profiles FOR ALL USING (auth.uid() = id);

-- Answer attempts: own only
CREATE POLICY "Users manage own attempts" ON answer_attempts FOR ALL USING (auth.uid() = user_id);

-- Topic mastery: own only
CREATE POLICY "Users manage own mastery" ON topic_mastery FOR ALL USING (auth.uid() = user_id);

-- User badges: own view
CREATE POLICY "Users view own badges" ON user_badges FOR SELECT USING (auth.uid() = user_id);

-- XP: own view
