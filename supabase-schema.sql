-- Enable UUID extension
CREATE EXTENSION IF NOT EXISTS "uuid-ossp";

-- =============================================
-- AUTH & USERS
-- =============================================

-- User profiles (extends Supabase auth.users)
CREATE TABLE profiles (
  id UUID PRIMARY KEY REFERENCES auth.users(id) ON DELETE CASCADE,
  email TEXT NOT NULL,
  display_name TEXT,
  matric_number TEXT,
  avatar_url TEXT,
  is_whitelisted BOOLEAN DEFAULT FALSE,
  is_admin BOOLEAN DEFAULT FALSE,
  
  -- Gamification
  xp INTEGER DEFAULT 0,
  level INTEGER DEFAULT 1,
  streak_current INTEGER DEFAULT 0,
  streak_longest INTEGER DEFAULT 0,
  streak_last_activity DATE,
  
  -- Stats
  questions_answered INTEGER DEFAULT 0,
  questions_correct INTEGER DEFAULT 0,
  total_study_time_minutes INTEGER DEFAULT 0,
  
  created_at TIMESTAMPTZ DEFAULT NOW(),
  updated_at TIMESTAMPTZ DEFAULT NOW()
);

-- Email whitelist (for non-NUS emails you want to allow)
CREATE TABLE email_whitelist (
  id UUID PRIMARY KEY DEFAULT uuid_generate_v4(),
  email TEXT UNIQUE NOT NULL,
  added_by UUID REFERENCES profiles(id),
  note TEXT,
  created_at TIMESTAMPTZ DEFAULT NOW()
);

-- =============================================
-- QUESTION BANK
-- =============================================

-- Systems taxonomy
CREATE TYPE question_system AS ENUM (
  'Foundation',
  'Blood',
  'Cardiovascular',
  'Renal', 
  'Respiratory',
  'Gastrointestinal',
  'Metabolism',
  'Immunology',
  'Pharmacology',
  'Pathology',
  'Mixed'
);

-- Question types
CREATE TYPE question_type AS ENUM ('MCQ', 'MEQ');

-- Difficulty tiers (matching YLL structure)
CREATE TYPE difficulty_tier AS ENUM (
  'Tier1_Recall',        -- 20% of YLL exams
  'Tier2_Application',   -- 50% of YLL exams
  'Tier3_Integration'    -- 30% of YLL exams
);

-- Question source
CREATE TYPE question_source AS ENUM (
  'PYP_Extracted',    -- Directly from past year papers
  'AI_Generated',     -- Generated by Claude based on PYP style + Anki
  'Recall_Derived',   -- Derived from student recalls
  'Manual'            -- Manually added
);

-- Main questions table
CREATE TABLE questions (
  id UUID PRIMARY KEY DEFAULT uuid_generate_v4(),
  
  -- Content
  question_type question_type NOT NULL DEFAULT 'MCQ',
  system question_system NOT NULL,
  topic TEXT NOT NULL,               -- e.g. "Glomerulonephritis", "MI Mechanism"
  subtopic TEXT,                     -- e.g. "IgA Nephropathy", "STEMI"
  
  -- Question body
  vignette TEXT,                     -- Clinical scenario (may be null for pure recall Qs)
  question_text TEXT NOT NULL,       -- The actual question
  image_url TEXT,                    -- For histology slides, X-rays, ECGs
  image_caption TEXT,
  
  -- MCQ options (stored as JSONB array)
  -- Format: [{"id": "A", "text": "..."}, {"id": "B", "text": "..."}, ...]
  options JSONB,                     -- NULL for MEQ
  correct_option TEXT,               -- "A"-"E" for MCQ, NULL for MEQ
  
  -- MEQ answer (for MEQ type)
  meq_model_answer TEXT,
  meq_marking_points JSONB,          -- [{"point": "...", "marks": 1}, ...]
  meq_total_marks INTEGER,
  
  -- Explanation (shown after answering)
  explanation TEXT NOT NULL,
  explanation_mechanism TEXT,        -- The "why" — mechanism-focused
  high_yield_pearl TEXT,            -- The one-liner to remember
  
  -- Metadata
  difficulty difficulty_tier NOT NULL DEFAULT 'Tier2_Application',
  source question_source NOT NULL DEFAULT 'PYP_Extracted',
  source_paper TEXT,                 -- e.g. "Phase I EOY 2023"
  source_year INTEGER,
  tags TEXT[],                       -- ['ECG', 'Pharmacology', 'Coagulation']
  
  -- High-yield scoring
  pyp_frequency INTEGER DEFAULT 1,   -- How many times this concept appeared in PYPs
  high_yield_score FLOAT GENERATED ALWAYS AS (
    CASE 
      WHEN difficulty = 'Tier2_Application' THEN pyp_frequency * 1.5
      WHEN difficulty = 'Tier3_Integration' THEN pyp_frequency * 2.0
      ELSE pyp_frequency * 1.0
    END
  ) STORED,
  
  -- Quality control
  is_verified BOOLEAN DEFAULT FALSE,
  is_active BOOLEAN DEFAULT TRUE,
  flagged_count INTEGER DEFAULT 0,
  
  created_at TIMESTAMPTZ DEFAULT NOW(),
  updated_at TIMESTAMPTZ DEFAULT NOW()
);

-- =============================================
-- USER PERFORMANCE TRACKING
-- =============================================

-- Every answer attempt
CREATE TABLE answer_attempts (
  id UUID PRIMARY KEY DEFAULT uuid_generate_v4(),
  user_id UUID NOT NULL REFERENCES profiles(id) ON DELETE CASCADE,
  question_id UUID NOT NULL REFERENCES questions(id) ON DELETE CASCADE,
  
  -- Answer
  selected_option TEXT,              -- MCQ: "A"-"E"
  meq_answer TEXT,                   -- MEQ: free text
  is_correct BOOLEAN,
  time_taken_seconds INTEGER,
  
  -- Session context
  session_type TEXT,                 -- 'practice', 'timed', 'daily_rounds', 'boss_battle'
  
  -- XP awarded
  xp_earned INTEGER DEFAULT 0,
  
  created_at TIMESTAMPTZ DEFAULT NOW()
);

-- Per-topic mastery tracking
CREATE TABLE topic_mastery (
  id UUID PRIMARY KEY DEFAULT uuid_generate_v4(),
  user_id UUID NOT NULL REFERENCES profiles(id) ON DELETE CASCADE,
  system question_system NOT NULL,
  topic TEXT NOT NULL,
  
  -- Mastery metrics
  attempts INTEGER DEFAULT 0,
  correct INTEGER DEFAULT 0,
  mastery_score FLOAT DEFAULT 0.0,   -- 0.0-1.0, computed
  mastery_level TEXT DEFAULT 'unstarted', -- unstarted, learning, familiar, mastered
  
  -- Spaced repetition
  next_review_date DATE,
  ease_factor FLOAT DEFAULT 2.5,     -- SM-2 algorithm
  interval_days INTEGER DEFAULT 1,
  
  last_attempted TIMESTAMPTZ,
  created_at TIMESTAMPTZ DEFAULT NOW(),
  updated_at TIMESTAMPTZ DEFAULT NOW(),
  
  UNIQUE(user_id, system, topic)
);

